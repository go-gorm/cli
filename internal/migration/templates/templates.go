`

// DiffHelperTemplate renders the generated diff helper file.
var DiffHelperTemplate = template.Must(template.New("diff-helper").Parse(diffHelperTemplateText))

const diffHelperTemplateText = `// Code generated by gorm migrate diff; DO NOT EDIT.
