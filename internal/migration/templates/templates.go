// RenderModelFile renders a complete model file with formatting.
func RenderModelFile(pkg, table, structName, fields string, imports []string) (string, error) {
	var buf bytes.Buffer
	sort.Strings(imports)

	structSrc, err := RenderModelStruct(structName, table, fields)
	if err != nil {
		return "", err
	}

	data := struct {
		Pkg     string
		Struct  string
		Imports []string
	}{
		Pkg:     pkg,
		Struct:  structSrc,
		Imports: imports,
	}

	if err := ModelFileTemplate.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute model file template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted if formatting fails
		return buf.String(), nil
	}

	return string(formatted), nil
}

// RenderModelStruct renders just the struct definition.
func RenderModelStruct(structName, table, fields string) (string, error) {
	var buf bytes.Buffer

	data := struct {
		StructName string
		Table      string
		Fields     string
	}{
		StructName: structName,
		Table:      table,
		Fields:     fields,
	}

	if err := ModelStructTemplate.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute model struct template: %w", err)
	}

	return buf.String(), nil
}

// RenderMigrationFile renders a migration file.
func RenderMigrationFile(name string) (string, error) {
	var buf bytes.Buffer

	data := struct{ Name string }{Name: name}

	if err := MigrationTemplate.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute migration template: %w", err)
	}

	return buf.String(), nil
}

// RenderDiffFile renders the diff helper file.
func RenderDiffFile(data interface{}) (string, error) {
	var buf bytes.Buffer

	if err := DiffHelperTemplate.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute diff helper template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return "", fmt.Errorf("format diff helper: %w", err)
	}

	return string(formatted), nil
}
